// ## 05 - Add Up

// - Créez une fonction `addUp` qui reçoit un paramètre `num`
// - À l'aide d'une boucle, ajouter les chiffres de 1 à `num`
// - Appelez votre fonction avec le chiffre 12 et vérifiez que vous obtenez 78

function addUp(num) {
    for (i = 0; i <= num; i++) {
         num = num + i
    }
}
addUp(12);


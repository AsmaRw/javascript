/*## 04 - Arithmétique

- Créer une variable `test` de valeur `12` et une variable `bis` de valeur `5`
- Utilisez tous les opérateurs arithmétiques sur ces variables 
- affichez les résultats*/

var test = 12;
var bis = 5;
console.log(test + bis)
console.log(test - bis)
console.log(test * bis)
console.log(test / bis)
console.log(test ** bis)
console.log(test % bis)
